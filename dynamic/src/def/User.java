package def;

public class User {
	
	public int id = 0;
	public String firstname = "";
	public String lastname = "";
	public String email = "";
	public String mobile = "";
	public String zip = "";
	public String street = "";
	public String housenumber = "";
	public String creditcardnumber = "";
	public String ccexpirem = "";
	public String ccexpirey = "";
	public String cvv = "";
	public String username = "";
	public String password = "";
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFirstname() {
		return firstname;
	}
	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}
	public String getLastname() {
		return lastname;
	}
	public void setLastname(String lastname) {
		this.lastname = lastname;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getZip() {
		return zip;
	}
	public void setZip(String zip) {
		this.zip = zip;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	public String getHousenumber() {
		return housenumber;
	}
	public void setHousenumber(String housenumber) {
		this.housenumber = housenumber;
	}
	public String getCreditcardnumber() {
		return creditcardnumber;
	}
	public void setCreditcardnumber(String creditcardnumber) {
		this.creditcardnumber = creditcardnumber;
	}
	public String getCcexpirem() {
		return ccexpirem;
	}
	public void setCcexpirem(String ccexpirem) {
		this.ccexpirem = ccexpirem;
	}
	public String getCcexpirey() {
		return ccexpirey;
	}
	public void setCcexpirey(String ccexpirey) {
		this.ccexpirey = ccexpirey;
	}
	public String getCvv() {
		return cvv;
	}
	public void setCvv(String cvv) {
		this.cvv = cvv;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
}
